Num1 = float(input("Enter first number: "))
op = input('Enter operator: ')
Num2 = float(input('Enter second number: '))



if op == "+":
    print(Num1 + Num2)

elif op == "-":
    print(Num1 - Num2)

elif op == "*":
    print(Num1 * Num2)

elif op == "/":
    print(Num1 / Num2)

else:
    print("Invalid Operator")





     

